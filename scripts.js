var previousSize = 0
const incrementPoint = () => {
    let size = screenCssPixelRatio = (window.outerWidth - 8) / window.innerWidth;
    if (size != previousSize) {
        previousSize = size
        centerMap()
    };
};
setInterval(incrementPoint, 20);
